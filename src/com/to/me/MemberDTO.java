package com.to.me;

public class MemberDTO {
	private String id;
	private String pw;
	private String name;
	private String email;
	private String birth;
	private String pw_que;
	private String pw_ans;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getPw_que() {
		return pw_que;
	}
	public void setPw_que(String pw_que) {
		this.pw_que = pw_que;
	}
	public String getPw_ans() {
		return pw_ans;
	}
	public void setPw_ans(String pw_ans) {
		this.pw_ans = pw_ans;
	}
	
	
}
